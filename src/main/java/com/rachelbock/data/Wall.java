package com.rachelbock.data;

/**
 * Class to hold Wall data
 */
public class Wall {

    protected int id;
    protected String name;



    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
